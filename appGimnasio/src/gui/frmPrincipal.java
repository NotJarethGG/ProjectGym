    private void btnSalirSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalirSesionActionPerformed
        int resp = JOptionPane.showConfirmDialog(null, "¿Seguro que desea cerrar sesión?", "Salir", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (resp == JOptionPane.YES_OPTION) {
            try {
                frmLogin log = new frmLogin();
                log.setVisible(true);
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(frmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnSalirSesionActionPerformed

    private void mnuVistaCascadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuVistaCascadaActionPerformed
        int xOffset = 30, yOffset = 30;
        JInternalFrame objetos[] = escritorio.getAllFrames();
        for (JInternalFrame v : objetos) {
            v.setLocation(xOffset += 25, yOffset += 30);
            try {
                v.setSelected(true);
            } catch (PropertyVetoException ex) {
                Logger.getLogger(frmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_mnuVistaCascadaActionPerformed

