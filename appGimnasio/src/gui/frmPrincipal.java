    public void CargarUsuario() {
        Conexion con = new Conexion();
        con.conectarBD();

        try {
            //llamado de la clase conexion con el metodo db que contiene la conexion de a  la db

            PreparedStatement pst = con.getConexion().prepareStatement(
                    "Select nombre from tblusers where users = '" + users + "'");

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                nombreUsuario = rs.getString("nombre");
                lblNombreUsuario.setText("Usuario: " + nombreUsuario);
//                    jMenu1.setText("Sesión de " + users);
            }

        } catch (SQLException e) {
            System.err.println("Error en conexion desde la interfaz administrador ");
        }

    }

