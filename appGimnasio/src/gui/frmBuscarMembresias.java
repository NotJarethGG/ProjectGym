    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtConsulta.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese un valor de busqueda");
        } else {
            gestionMembresias.conectarBD();
            if (cmbBuscarMembresia.getSelectedIndex() == 0) {
                int idMembresia = Integer.parseInt(txtConsulta.getText());
                ResultSet rs = gestionMembresias.seleccionaridMembresia(idMembresia);
                if (rs != null) {
                    tblDatos.setModel(gestionMembresias.cargarEnTabla(rs));
                    lbTotal.setText("Total: " + String.valueOf(tblDatos.getRowCount()));

                } else {
                    JOptionPane.showMessageDialog(rootPane, "No se ha encontrado el id de la membresia");
                }

                gestionMembresias.desconectarBD();
            }
            if (cmbBuscarMembresia.getSelectedIndex() == 1) {

                String nombre = txtConsulta.getText();
                ResultSet rs = gestionMembresias.seleccionarNombre(nombre);

                if (rs != null) {
                    tblDatos.setModel(gestionMembresias.cargarEnTabla(rs));
                    lbTotal.setText("Total: " + String.valueOf(tblDatos.getRowCount()));
                } else {
                    JOptionPane.showMessageDialog(rootPane, "No se ha encontrado el nombre de la membresia");
                }
                gestionMembresias.desconectarBD();

            }
            if (cmbBuscarMembresia.getSelectedIndex() == 2) {

                String periodo = txtConsulta.getText();
                ResultSet rs = gestionMembresias.seleccionarPeriodo(periodo);

                if (rs != null) {
                    tblDatos.setModel(gestionMembresias.cargarEnTabla(rs));
                    lbTotal.setText("Total: " + String.valueOf(tblDatos.getRowCount()));
                } else {
                    JOptionPane.showMessageDialog(rootPane, "No se ha encontrado la membresia");
                }
                gestionMembresias.desconectarBD();

            }

        }

    }//GEN-LAST:event_btnBuscarActionPerformed

