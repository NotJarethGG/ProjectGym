    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtConsulta.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese un valor de busqueda");
        } else {

            gestionRegistroIngreso.conectarBD();
            if (cmbBuscarRegistro.getSelectedIndex() == 0) {
                int idRegistroIngreso = Integer.parseInt(txtConsulta.getText());
                ResultSet rs = gestionRegistroIngreso.seleccionarIdRegistroIngreso(idRegistroIngreso);
                if (rs != null) {
                    tblDatos.setModel(gestionRegistroIngreso.cargarEnTabla(rs));
                    lbTotal.setText("Total: " + String.valueOf(tblDatos.getRowCount()));

                } else {
                    JOptionPane.showMessageDialog(rootPane, "No se ha encontrado el ID ingreso");
                }

                gestionRegistroIngreso.desconectarBD();
            }
            if (cmbBuscarRegistro.getSelectedIndex() == 1) {
                int idSocio = Integer.parseInt(txtConsulta.getText());
                ResultSet rs = gestionRegistroIngreso.seleccionarIdSocio(idSocio);
                if (rs != null) {
                    tblDatos.setModel(gestionRegistroIngreso.cargarEnTabla(rs));
                    lbTotal.setText("Total: " + String.valueOf(tblDatos.getRowCount()));
                } else {
                    JOptionPane.showMessageDialog(rootPane, "No se ha encontrado el ID de socio");
                }
                gestionRegistroIngreso.desconectarBD();

            }
            if (cmbBuscarRegistro.getSelectedIndex() == 2) {

                String fecha = txtConsulta.getText();
                ResultSet rs = gestionRegistroIngreso.seleccionarFecha(fecha);
                JOptionPane.showConfirmDialog(rootPane, fecha);
                if (rs != null) {
                    tblDatos.setModel(gestionRegistroIngreso.cargarEnTabla(rs));
                    lbTotal.setText("Total: " + String.valueOf(tblDatos.getRowCount()));
                } else {
                    JOptionPane.showMessageDialog(rootPane, "No se ha encontrado la Fecha");
                }
                gestionRegistroIngreso.desconectarBD();

            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

